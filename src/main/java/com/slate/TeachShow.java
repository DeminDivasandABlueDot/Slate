package com.slate;

public class TeachShow {
	String TeachName;
	String TeachID;
	String SubName;
	String SubID;
	int HoursPerWeek;
	public TeachShow(String TeachName, String TeachID, String SubName, String SubID, int HoursPerWeek) {
		this.TeachName=TeachName;
		this.TeachID=TeachID;
		this.SubName=SubName;
		this.SubID=SubID;
		this.HoursPerWeek=HoursPerWeek;
	}
}
